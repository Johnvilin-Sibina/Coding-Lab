// Write a calculator program to perfom any one Mathematical operation which is user's choice

let a = 12;
let b = 3;
let operator =prompt("Enter the operation that need to be performed:");
if(operator == '+'){
    console.log(a+b)
}
else if(operator == '-'){
    console.log(a-b)
}
else if(operator == '*'){
    console.log(a*b)
}
else if(operator == '/'){
    console.log(a/b)
}
    

